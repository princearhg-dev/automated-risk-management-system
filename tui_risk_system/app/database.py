def acknowledge_alert(alert_id):
    conn = get_connection()
    conn.execute(
        "UPDATE alerts SET acknowledged = 1 WHERE id = ?", (alert_id,)
    )
    conn.commit()
    conn.close()


def get_alert_counts_by_severity():
    # returns a dict of severity -> count for the dashboard summary
    conn = get_connection()
    rows = conn.execute(
        "SELECT severity, COUNT(*) as count FROM alerts GROUP BY severity"
    ).fetchall()
    conn.close()
    return {r["severity"]: r["count"] for r in rows}


def get_alerts_over_time(hours=24):
    # groups alert counts by hour for the time-series chart
    # the SQLite strftime function handles the grouping nicely
    conn = get_connection()
    rows = conn.execute(
        """SELECT strftime('%Y-%m-%d %H:00', timestamp) as hour,
                  COUNT(*) as count
           FROM alerts
           WHERE timestamp >= datetime('now', ? || ' hours')
           GROUP BY hour
           ORDER BY hour""",
        (f"-{hours}",)
    ).fetchall()
    conn.close()
    return [dict(r) for r in rows]


