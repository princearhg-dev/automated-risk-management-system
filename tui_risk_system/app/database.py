def get_dashboard_summary():
    # pulls together the key numbers for the stat cards at the top of the dashboard
    conn = get_connection()

    total_events = conn.execute("SELECT COUNT(*) FROM events").fetchone()[0]
    total_alerts = conn.execute("SELECT COUNT(*) FROM alerts").fetchone()[0]
    unacked_alerts = conn.execute(
        "SELECT COUNT(*) FROM alerts WHERE acknowledged = 0"
    ).fetchone()[0]

    # get the highest risk level currently in the system
    highest = conn.execute(
        "SELECT risk_level FROM risk_scores ORDER BY risk_score DESC LIMIT 1"
    ).fetchone()

    conn.close()

    return {
        "total_events": total_events,
        "total_alerts": total_alerts,
        "unacknowledged_alerts": unacked_alerts,
        "highest_risk_level": highest["risk_level"] if highest else "N/A",
    }
